import os
import shutil

folder = 'my_project'
for root, dirs, files in os.walk(folder):
    for item in dirs:
        if item == 'templates':
           shutil.copytree(os.path.join(root, item),
                           os.path.join(folder, 'templates'),
                           dirs_exist_ok=True)
